# Escribir un programa que muestre por pantalla el resultado de la siguiente operación aritmética ((2+2)/(2.5))^2

Numerador = 3+2
denominador = 2*5
frac = (Numerador / denominador)

# La función pow() se usa para elevar un numero a una potencia específica: pow(base, exponente)
potencia = pow(frac, 2)

print(potencia)
